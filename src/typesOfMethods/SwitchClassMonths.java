package typesOfMethods;

public class SwitchClassMonths {
    public static void main(String[] args) {
        int Months = 10;
        switch (Months) {
            //january
            case 1:
                System.out.println(" 1 january" + "wedding anniversary");
                break;
            // february
            case 2:
                System.out.println(" 2 february");
                break;
            // march
            case 3:
                System.out.println(" 3 march" + " hrishi birthday month");

            // april
            case 4:
                System.out.println(" 4 april");
                break;
            //may
            case 5:
                System.out.println(" 5 may");
                break;
            //june
            case 6:
                System.out.println(" 6 june");
                break;
            // july
            case 7:
                System.out.println(" 7 july");
                break;
            // august
            case 8:
                System.out.println(" 8 august");
                break;
            // september
            case 9:
                System.out.println(" 9 september");
                break;
            // october
            case 10:
                System.out.println(" 10 october  "+ "  ketan &  ishan birthday month");


            // november
            case 11:
                System.out.println(" 11 november  " +"  my  birthday  month");
                break;
            // december
            case 12:
                System.out.println(" 12 december");
                break;
            default:
                System.out.println("invalid month");


        }
    }


}